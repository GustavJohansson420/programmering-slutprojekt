class Player():
    def __init__(self, x, y):
        img = pygame.image.load('bilder/DevilShit.png')
        self.image = pygame.transform.scale(img, (24, 48))
        self.rect = self.image.get_rect()
        self.rect.x = x
        self.rect.y = y

    def update(self):
        dx = 0
        dy = 0
        key = pygame.key.get_pressed()
        if key[pygame.K_a]:
            dx -= 3
        if key[pygame.K_d]:
            dx += 3
        self.rect.x += dx
        self.rect.y += dy
        screen.blit(self.image, self.rect)

class World():
    def __init__(self, data):
        self.tile_list = []
        Gräs_Block = pygame.image.load('bilder/Gräs_Block.png')
        Jord_Block = pygame.image.load('bilder/Jord_Block.png')
        row_count = 0
        for row in data:
            col_count = 0
            for tile in row:
                if tile == 1:
                    img = pygame.transform.scale(Gräs_Block, (tile_size, tile_size))
                    img_rect = img.get_rect()
                    img_rect.x = col_count * tile_size
                    img_rect.y = row_count * tile_size
                    tile = (img, img_rect)
                    self.tile_list.append(tile)
                if tile == 2:
                    img = pygame.transform.scale(Jord_Block, (tile_size, tile_size))
                    img_rect = img.get_rect()
                    img_rect.x = col_count * tile_size
                    img_rect.y = row_count * tile_size
                    tile = (img, img_rect)
                    self.tile_list.append(tile)
                col_count += 1
            row_count += 1

    def draw(self):
        for tile in self.tile_list:
            screen.blit(tile[0], tile[1])

